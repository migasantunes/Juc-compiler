class ExprOrAndPrecedence {
    public static void main(String[] args) {
        System.out.print(true || false && false);
        System.out.print("\n");
        System.out.print(false && true || true);
        System.out.print("\n");
        System.out.print(false || false && true);
        System.out.print("\n");
    }
}
