class ExprMixedCompare {
    public static void main(String[] args) {
        System.out.print(5 > 3.0);
        System.out.print("\n");
        System.out.print(5.0 == 5);
        System.out.print("\n");
        System.out.print((1.5 + 1.5) <= 3.0);
        System.out.print("\n");
        System.out.print(-1.0 < 0);
        System.out.print("\n");
    }
}
