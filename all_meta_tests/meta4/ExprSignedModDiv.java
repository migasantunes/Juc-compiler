class ExprSignedModDiv {
    public static void main(String[] args) {
        int a;
        int b;
        int c;
        a = -7;
        b = 3;
        c = 85;
        System.out.print(a / b);
        System.out.print("\n");
        System.out.print(a % b);
        System.out.print("\n");
        System.out.print(c % -2);
        System.out.print("\n");
        System.out.print((-85) % 2);
        System.out.print("\n");
    }
}
