class MainShiftMask {
    public static void main(String[] args) {
        System.out.print(1 << 33);
        System.out.print("\n");
        System.out.print(-8 >> 33);
        System.out.print("\n");
        int s;
        s = 36;
        System.out.print(1 << s);
        System.out.print("\n");
    }
}
